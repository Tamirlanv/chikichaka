export type AddressOption = { value: string; label: string };

const PLACEHOLDER_REGION: AddressOption = { value: "", label: "Выберите регион" };
const PLACEHOLDER_CITY: AddressOption = { value: "", label: "Выберите город" };

/** Если в черновике уже есть текст, которого нет в справочнике — показываем его отдельной опцией. */
export function mergeSavedOption(options: AddressOption[], saved: string | undefined): AddressOption[] {
  const t = (saved ?? "").trim();
  if (!t) return options;
  if (options.some((o) => o.value === t)) return options;
  return [{ value: t, label: `${t} (из сохранённых данных)` }, ...options];
}

export function mergeSavedCityOptions(options: AddressOption[], saved: string | undefined): AddressOption[] {
  const t = (saved ?? "").trim();
  if (!t) return options;
  if (options.some((o) => o.value === t)) return options;
  return [{ value: t, label: `${t} (из сохранённых данных)` }, ...options];
}

