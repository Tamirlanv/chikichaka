export function getRegionsForCountry(country: string): AddressOption[] {
  switch (country) {
    case "KZ":
      return [PLACEHOLDER_REGION, ...KZ_REGIONS];
    case "KG":
      return [PLACEHOLDER_REGION, ...KG_REGIONS];
    case "UZ":
      return [PLACEHOLDER_REGION, ...UZ_REGIONS];
    case "RU":
      return [PLACEHOLDER_REGION, ...RU_REGIONS];
    default:
      return [PLACEHOLDER_REGION];
  }
}

export function getCitiesForCountryAndRegion(country: string, region: string): AddressOption[] {
  const r = (region ?? "").trim();
  if (!r) {
    return [PLACEHOLDER_CITY];
  }
  let cities: AddressOption[] = [];
  switch (country) {
    case "KZ":
      cities = kzCities(r);
      break;
    case "KG":
      cities = (KG_DATA[r] ?? []).map((c) => ({ value: c, label: c }));
      break;
    case "UZ":
      cities = (UZ_DATA[r] ?? []).map((c) => ({ value: c, label: c }));
      break;
    case "RU":
      cities = (RU_DATA[r] ?? []).map((c) => ({ value: c, label: c }));
      break;
    default:
      cities = [];
  }
  if (cities.length === 0) {
    return [PLACEHOLDER_CITY];
  }
  return [PLACEHOLDER_CITY, ...cities];
}
